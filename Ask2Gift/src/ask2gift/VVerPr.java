    public void rellenarCombo() {
        DefaultComboBoxModel modelo = new DefaultComboBoxModel();//Rellenar JComboBox
        for (Categoria cat : vprincipal.getCategorias()) {
            modelo.addElement(cat.getNombre());
        }

        jComboBox1.setModel(modelo);
        rellenarareaTexto();
    }

    public void rellenarareaTexto() {
        Categoria c = new Categoria();
        String cadena = "";
        String nomCat = (String) jComboBox1.getSelectedItem();
        c.setNombre(nomCat);
        int indexCat = vprincipal.getCategorias().indexOf(c);
        c = categorias.get(indexCat);
        for (Pregunta p : c.getPreguntas()) {

            cadena += p.getTexto_pr();
            cadena += "\n";

            for (Respuesta r : p.getRespuestas()) {
                cadena += "    ";
                cadena += r.getValor();
                cadena += " - ";
                cadena += r.getTexto_rp();
                cadena += "\n";

            }

        }
        jTextArea1.setText(cadena);
    }

